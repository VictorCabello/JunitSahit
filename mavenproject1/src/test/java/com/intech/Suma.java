
package com.intech;

public class Suma
{
    private int sumandoA;
    private int sumandoB;
    private int total;

    public int getSumandoA()
    {
        return sumandoA;
    }

    public void setSumandoA(int sumandoA)
    {
        this.sumandoA = sumandoA;
    }

    public int getSumandoB()
    {
        return sumandoB;
    }

    public void setSumandoB(int sumandoB)
    {
        this.sumandoB = sumandoB;
    }

    public int getTotal()
    {
        return total;
    }

    public void setTotal(int total)
    {
        this.total = total;
    }
}
